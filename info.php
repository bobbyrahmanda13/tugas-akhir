          <p class="style2">Skoliosis</p>
          <p>Skoliosis adalah kondisi  melengkungnya tulang belakang ke samping secara tidak normal. Penyakit ini  lebih sering terjadi pada anak-anak sebelum masa pubertas dengan kisaran usia  10 hingga 15 tahun. Anak laki-laki dan perempuan memiliki risiko yang sama untuk  mengidap skoliosis ringan, namun anak perempuan lebih rentan untuk mengalami  perburukan gejala sehingga memerlukan penanganan yang memadai. Meskipun  kebanyakan kasus skoliosis yang menimpa anak-anak bersifat ringan dan tidak  memerlukan perawatan, namun harus diawasi secara saksama dan disarankan untuk  menjalani&nbsp;<em>X-ray</em>&nbsp;secara  rutin untuk mengetahui perkembangannya dan menghindari komplikasi lanjutan.Sulit  bernapas dan nyeri hebat biasanya akan dirasakan oleh penderita skoliosis  remaja dan dewasa jika tulang belakang yang melengkung bertambah parah. Oleh  karena itu, deteksi dini diperlukan untuk mencegah skoliosis mencapai tingkat  keparahan lebih lanjut.</p>
          <p><strong>Osteomielitis</strong></p>
          <p>Osteomielitis adalah infeksi tulang yang disebabkan oleh bakteri. Ada dua jenis osteomielitis, yaitu akut dan kronis. Gejala osteomielitis akut bisa berupa nyeri tulang, demam, serta pembengkakan dan kemerahan pada area yang terinfeksi. Biasanya nyeri tulang yang dirasakan pada kondisi ini cenderung parah. Selain gejala-gejala tersebut, osteomielitis akut bisa ditandai dengan pembengkakan kelenjar getah bening di sekitar area yang terinfeksi sehingga membatasi pergerakan tubuh. Osteomielitis akut kerap menimpa tulang panjang di daerah kaki. Selain bagian tersebut, tulang punggung dan lengan juga menjadi lokasi yang biasanya kena dampak.</p>
          <p><strong>Osteoporosis</strong></p>
          <p>Osteoporosis adalah kondisi saat kualitas kepadatan tulang menurun. Kondisi ini membuat tulang menjadi keropos dan rentan retak. Osteoporosis umumnya baru diketahui setelah ditemukan retak pada tulang, setelah pasien mengalami jatuh ringan. Retak pada pergelangan tangan, tulang pinggul, dan tulang belakang adalah kasus yang paling banyak ditemui pada penderita osteoporosis. Di Indonesia, sebanyak 23 persen wanita berusia 50-80 tahun dan 53 persen wanita berusia 70-80 tahun mengidap osteoporosis, berdasarkan hasil penelitian pada tahun 2006. Risiko wanita mengidap osteoporosis empat kali lebih besar dibandingkan dengan risiko pada pria.</p>
