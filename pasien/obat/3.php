<div align="justify">Penyakit Meniere termasuk penyakit yang tidak bisa disembuhkan.   Pengobatannya bertujuan untuk mengendalikan gejala-gejalanya.   Langkah-langkah penanganan yang umumnya dapat dijalani oleh pasien   adalah:
  <ul>
    <li>Memperbaiki pola makan, khususnya mengurangi konsumsi garam.</li>
    <li>Pemberian obat-obatan guna meredakan atau mencegah serangan, contohnya vertigo.</li>
    <li>Menangani tinnitus, contohnya terapi suara, teknik relaksasi, serta terapi perilaku kognitif (CBT).</li>
    <li>Terapi rehabilitasi vestibular untuk mengatasi gangguan keseimbangan.</li>
    <li>Mengatasi kehilangan pendengaran, misalnya dengan alat bantu dengar.</li>
    <li>Menangani atau mencegah stres dan depresi.</li>
  </ul>
  Apabila langkah-langkah tersebut terbukti tidak efektif, khususnya pada   pasien dengan penyakit Meniere yang parah, pilihan operasi biasanya akan   dianjurkan. Beberapa pilihan operasi ini antara lain:
  <ul>
    <li>Prosedur untuk mengurangi cairan atau tekanan pada telinga bagian   dalam. Pilihan operasi ini bertujuan mengurangi atau meredakan gejala   sekaligus menjaga kemampuan pendengaran.</li>
    <li>Penyuntikan steroid ke telinga bagian dalam. Operasi ini masih memerlukan penelitian untuk membuktikan efektivitasnya.</li>
    <li>Operasi pemotongan saraf yang menghubungkan keseimbangan dan saraf vestibular.</li>
    <li>Prosedur untuk menghancurkan pusat keseimbangan tubuh dengan pengangkatan labirin (<em>labyrinthectomy</em>) atau ablasi menggunakan senyawa kimia dari antibiotik seperti gentamicin.</li>
  </ul>
